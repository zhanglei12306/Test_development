# int 整型  整数
age = 18
print(type(age))   # <class 'int'>

# float 浮点型  小数
height = 1.71
print(type(height))  # <class 'float'>

# bool 类型,True(真) 和 False(假), 都是关键字
# is_men = True
is_men = False
print(type(is_men))   # <class 'bool'>

# 字符串 str, 使用 引号引起来的就是字符串
name = 'tom'
print(type(name))

num = '20'
print(type(num))   # <class 'str'>

