# 取款机取钱的过程, 假定 你的密码是: 123456,  账户余额为 1000
# 1. 提示用户输入密码
pwd = input('请输入密码:')
# 2. 判断密码是否正确
if pwd == '123456':
    print('密码输入正确')
    # 3. 密码正确后,提示输入取款的金额,
    money = int(input('请输入取款的金额:'))
    # 4. 判断取款的金额和余额的关系
    if money > 1000:
        print('余额不足')
    else:
        print('取款中,请稍后....')
        print('请收好你的钱....')
else:
    print('密码输入错误,请重试')
