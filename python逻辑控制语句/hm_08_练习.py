# 假定某网站用户名固定为 'admin', 密码固定为'123456', 验证码 固定为 '8888'
# 1. 获取用户输入的用户名,密码和验证码
username = input('请输入用户名:')
password = input('请输入密码:')
code = input('请输入验证码:')
# 2. 先判断验证码是否正确,如果正确打印输出验证码正确,再判断用户名和密码是否正确
if code == '8888':
    print('验证码正确')
    if username == 'admin' and password == '123456':
        print('登录成功')
    else:
        print('用户名或密码错误')
# 3. 如果验证吗不正确,直接输出 验证码不正确,请重新输入
else:
    print('验证码不正确,请重新输入')
